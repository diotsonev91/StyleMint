/**
 * Complete checkout flow:
 * 1. Prepare order from cart
 * 2. Create order in backend
 * 3. Create Stripe checkout session
 * 4. Redirect to Stripe checkout
 * 5. Clear cart on success
 */
export async function proceedToCheckout(
  customerEmail?: string
): Promise<void> {
  try {
    console.log("üõí Starting checkout process...");

    // Step 1: Prepare order from cart
    const order = prepareOrderFromCart();

    if (order.items.length === 0) {
      throw new Error("Cart is empty");
    }

    console.log("üì¶ Order prepared:", order);
    console.log(`   - ${order.items.filter(i => i.productType === 'clothes').length} clothes items`);
    console.log(`   - ${order.items.filter(i => i.productType === 'sample').length} sample items`);
    console.log(`   - ${order.items.filter(i => i.productType === 'pack').length} pack items`);
    console.log(`   - Total: $${order.totalPrice.toFixed(2)}`);

    // Step 2: Create order in backend (saves to DB)
    const orderId = await createOrder(order);
    console.log("‚úÖ Order created with ID:", orderId);

    // Step 3: Create Stripe checkout session
    const { checkoutUrl, sessionId } = await createCheckoutSession(
      orderId,
      customerEmail
    );
    console.log("üí≥ Checkout session created:", sessionId);

    // Step 4: Store order ID in localStorage (for post-payment redirect)
    localStorage.setItem("pendingOrderId", orderId);

    // Step 5: Redirect to Stripe Checkout
    console.log("üîÑ Redirecting to Stripe Checkout...");
    window.location.href = checkoutUrl;

    // Cart will be cleared after successful payment (on return from Stripe)
  } catch (error) {
    console.error("‚ùå Checkout failed:", error);
    throw error;
  }
}

/**
 * Handle return from Stripe (success page)
 * Call this on your success page
 */
export async function handlePaymentSuccess(): Promise<void> {
  const orderId = localStorage.getItem("pendingOrderId");

  if (!orderId) {
    console.warn("‚ö†Ô∏è No pending order found");
    return;
  }

  try {
    // Verify payment completed
    const isPaid = await verifyPayment(orderId);

    if (isPaid) {
      console.log("‚úÖ Payment verified! Processing order...");

      // Get order details
      const order = await getOrderStatus(orderId);
      
      // Move items to purchase history
      const purchasedItems = cartState.items.map(item => ({ ...item }));
      cartState.purchaseHistory.push(...purchasedItems);

      // Clear cart
      cartState.items = [];

      // Clear pending order
      localStorage.removeItem("pendingOrderId");

      console.log("üéâ Order completed successfully!");
      console.log(`   - Order ID: ${orderId}`);
      console.log(`   - Total: $${order.totalPrice.toFixed(2)}`);
    } else {
      console.warn("‚ö†Ô∏è Payment not confirmed yet");
    }
  } catch (error) {
    console.error("‚ùå Failed to verify payment:", error);
  }
}

