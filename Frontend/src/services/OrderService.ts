/**
 * Get total item count (including quantities)
 */
export function getTotalItemCount(): number {
  return cartState.items.reduce((total, item) => {
    return total + ((item as any).quantity || 1);
  }, 0);
}

/**
 * Calculate total price (if you have pricing)
 */
export function calculateTotalPrice(pricePerItem = 29.99): number {
  return cartState.items.reduce((total, item) => {
    const quantity = (item as any).quantity || 1;
    return total + pricePerItem * quantity;
  }, 0);
}

/**
 * Clear cart (after successful order)
 */
export function clearCart(): void {
  cartState.items = [];
  console.log("üßπ Cart cleared");
}