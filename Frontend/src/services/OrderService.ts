/**
 * Get total item count (including quantities)
 */
export function getTotalItemCount(): number {
  return cartState.items.reduce((total, item) => {
    return total + (item.quantity || 1);
  }, 0);
}

/**
 * Calculate total price for all items in cart
 */
export function calculateTotalPrice(): number {
  return cartState.items.reduce((total, item) => {
    const quantity = item.quantity || 1;
    
    // Get price based on item type
    let pricePerUnit = 0;
    switch (item.type) {
      case 'clothes':
        pricePerUnit = 29.99; // Default clothes price
        break;
      case 'sample':
        pricePerUnit = item.price;
        break;
      case 'pack':
        pricePerUnit = item.price;
        break;
    }
    
    return total + (pricePerUnit * quantity);
  }, 0);
}

/**
 * Get breakdown of cart by product type
 */
export function getCartBreakdown() {
  const breakdown = {
    clothes: {
      count: 0,
      total: 0,
    },
    samples: {
      count: 0,
      total: 0,
    },
    packs: {
      count: 0,
      total: 0,
    },
  };

  cartState.items.forEach(item => {
    const quantity = item.quantity || 1;
    
    switch (item.type) {
      case 'clothes':
        breakdown.clothes.count += quantity;
        breakdown.clothes.total += 29.99 * quantity;
        break;
      case 'sample':
        breakdown.samples.count += quantity;
        breakdown.samples.total += item.price * quantity;
        break;
      case 'pack':
        breakdown.packs.count += quantity;
        breakdown.packs.total += item.price * quantity;
        break;
    }
  });

  return breakdown;
}

