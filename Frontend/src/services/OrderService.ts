/**
 * Validate cart before checkout
 */
export function validateCart(): { valid: boolean; errors: string[] } {
  const errors: string[] = [];

  if (cartState.items.length === 0) {
    errors.push("Cart is empty");
  }

  cartState.items.forEach(item => {
    // Validate quantity
    if (!item.quantity || item.quantity < 1) {
      errors.push(`Invalid quantity for item: ${item.id}`);
    }

    // Type-specific validation
    switch (item.type) {
      case 'clothes':
        if (!item.selectedColor) {
          errors.push(`Missing color for clothes item: ${item.id}`);
        }
        if (!item.selected_type) {
          errors.push(`Missing type for clothes item: ${item.id}`);
        }
        break;
      
      case 'sample':
        if (!item.name || item.price <= 0) {
          errors.push(`Invalid sample data: ${item.id}`);
        }
        break;
      
      case 'pack':
        if (!item.name || item.price <= 0) {
          errors.push(`Invalid pack data: ${item.id}`);
        }
        break;
    }
  });

  return {
    valid: errors.length === 0,
    errors,
  };
}