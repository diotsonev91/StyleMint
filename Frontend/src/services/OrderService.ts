// =====================================================
// UPDATE ITEM QUANTITY (SYNC WITH BACKEND OPTIONAL)
// =====================================================

/**
 * Update quantity and optionally sync with backend
 */
export async function updateItemQuantity(
  itemId: string,
  quantity: number,
  syncWithBackend = false
): Promise<void> {
  const itemIndex = cartState.items.findIndex((item) => item.id === itemId);

  if (itemIndex === -1) {
    throw new Error("Item not found");
  }

  const newQuantity = Math.max(1, quantity);
  (cartState.items[itemIndex] as any).quantity = newQuantity;

  console.log(`üìù Updated item ${itemId} quantity to ${newQuantity}`);

  // Optionally sync with backend (for persistent cart)
  if (syncWithBackend) {
    try {
      await fetch(`${BASE_URL}/cart/${itemId}`, {
        method: "PATCH",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ quantity: newQuantity }),
      });
      console.log("‚úÖ Quantity synced with backend");
    } catch (error) {
      console.error("‚ùå Failed to sync quantity with backend:", error);
    }
  }
}

