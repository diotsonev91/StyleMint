// OrderService.ts - Complete order and payment flow
import { cartState } from "../state";


const BASE_URL = "http://localhost:8080/api";

// =====================================================
// ORDER DATA TYPES
// =====================================================

export type OrderItem = {
  productId: string;
  productType: string;
  quantity: number;
  customization: {
    color: string;
    decal: string;
    decalPosition: [number, number, number] | null;
    rotationY: number;
    ripples: { id: number; pos: [number, number, number] }[];
  };
  pricePerUnit?: number; // Optional: if you have pricing
};

export type Order = {
  orderId?: string; // Generated by backend
  items: OrderItem[];
  totalQuantity: number;
  totalPrice?: number; // Optional: if you have pricing
  customerInfo?: {
    email?: string;
    name?: string;
  };
  status: "pending" | "processing" | "completed" | "failed";
  createdAt?: string;
  paymentIntentId?: string; // Stripe payment intent ID
};

export type CheckoutSessionResponse = {
  sessionId: string;
  checkoutUrl: string;
  orderId: string;
};

// =====================================================
// PREPARE ORDER FROM CART
// =====================================================

/**
 * Converts cart items to order format
 */
export function prepareOrderFromCart(): Order {
  const orderItems: OrderItem[] = cartState.items.map((item) => ({
    productId: item.id,
    productType: item.selected_type,
    quantity: (item as any).quantity || 1,
    customization: {
      color: item.selectedColor,
      decal: item.selectedDecal,
      decalPosition: item.decalPosition,
      rotationY: item.rotationY,
      ripples: item.ripples,
    },
    // pricePerUnit: 29.99, // Add if you have pricing
  }));

  const totalQuantity = orderItems.reduce(
    (sum, item) => sum + item.quantity,
    0
  );

  // Calculate total price (if you have pricing)
  // const totalPrice = orderItems.reduce(
  //   (sum, item) => sum + (item.pricePerUnit || 0) * item.quantity,
  //   0
  // );

  return {
    items: orderItems,
    totalQuantity,
    // totalPrice,
    status: "pending",
  };
}

// =====================================================
// BACKEND API CALLS
// =====================================================

/**
 * Create order in backend (saves to database)
 */
export async function createOrder(order: Order): Promise<string> {
  console.log("üì§ Sending order to backend:", order);

  const response = await fetch(`${BASE_URL}/orders`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify(order),
  });

  if (!response.ok) {
    throw new Error(`Failed to create order: ${response.statusText}`);
  }

  const data = await response.json();
  console.log("‚úÖ Order created:", data);
  return data.orderId; // Backend returns order ID
}

/**
 * Create Stripe checkout session (backend handles Stripe API)
 */
export async function createCheckoutSession(
  orderId: string,
  customerEmail?: string
): Promise<CheckoutSessionResponse> {
  console.log("üí≥ Creating Stripe checkout session for order:", orderId);

  const response = await fetch(`${BASE_URL}/checkout/create-session`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      orderId,
      customerEmail,
      // Backend will fetch order details and create Stripe session
    }),
  });

  if (!response.ok) {
    throw new Error(`Failed to create checkout session: ${response.statusText}`);
  }

  const data = await response.json();
  console.log("‚úÖ Checkout session created:", data);
  return data;
}

/**
 * Get order status
 */
export async function getOrderStatus(orderId: string): Promise<Order> {
  const response = await fetch(`${BASE_URL}/orders/${orderId}`);

  if (!response.ok) {
    throw new Error(`Failed to fetch order: ${response.statusText}`);
  }

  return response.json();
}

/**
 * Verify payment completion (webhook alternative)
 */
export async function verifyPayment(orderId: string): Promise<boolean> {
  const response = await fetch(`${BASE_URL}/orders/${orderId}/verify-payment`);

  if (!response.ok) {
    return false;
  }

  const data = await response.json();
  return data.paid === true;
}

