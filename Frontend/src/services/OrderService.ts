// OrderService.ts - Complete order and payment flow for ALL product types
import { cartState, CartItemState, ClothesCartItem, SampleCartItem, PackCartItem } from "../state";

const BASE_URL = import.meta.env.VITE_API_URL || "http://localhost:8080/api";

// =====================================================
// ORDER DATA TYPES
// =====================================================

// Base order item
type BaseOrderItem = {
  productId: string;
  productType: 'clothes' | 'sample' | 'pack';
  quantity: number;
  pricePerUnit?: number;
};

// Clothes order item
export type ClothesOrderItem = BaseOrderItem & {
  productType: 'clothes';
  customization: {
    color: string;
    decal: string;
    decalPosition: [number, number, number] | null;
    rotationY: number;
    ripples: { id: number; pos: [number, number, number] }[];
    selected_type: string;
  };
};

// Sample order item
export type SampleOrderItem = BaseOrderItem & {
  productType: 'sample';
  name: string;
  artist?: string;
  genre?: string;
  bpm?: number;
  key?: string;
  url?: string;
};

// Pack order item
export type PackOrderItem = BaseOrderItem & {
  productType: 'pack';
  name: string;
  artist?: string;
  sampleCount?: number;
  genres?: string[];
};

// Discriminated union of all order item types
export type OrderItem = ClothesOrderItem | SampleOrderItem | PackOrderItem;

export type Order = {
  orderId?: string; // Generated by backend
  items: OrderItem[];
  totalQuantity: number;
  totalPrice: number;
  customerInfo?: {
    email?: string;
    name?: string;
  };
  status: "pending" | "processing" | "completed" | "failed";
  createdAt?: string;
  paymentIntentId?: string; // Stripe payment intent ID
};

export type CheckoutSessionResponse = {
  sessionId: string;
  checkoutUrl: string;
  orderId: string;
};

// =====================================================
// PREPARE ORDER FROM CART
// =====================================================

/**
 * Converts cart items to order format
 * Handles all product types: clothes, samples, packs
 */
export function prepareOrderFromCart(): Order {
  const orderItems: OrderItem[] = cartState.items.map((item): OrderItem => {
    // Common fields
    const quantity = item.quantity || 1;

    // Handle different item types
    switch (item.type) {
      case 'clothes':
        return {
          productId: item.id,
          productType: 'clothes',
          quantity,
          customization: {
            color: item.selectedColor,
            decal: item.selectedDecal,
            decalPosition: item.decalPosition,
            rotationY: item.rotationY,
            ripples: item.ripples,
            selected_type: item.selected_type,
          },
          pricePerUnit: 29.99, // Default clothes price
        };

      case 'sample':
        return {
          productId: item.id,
          productType: 'sample',
          quantity,
          name: item.name,
          artist: item.artist,
          genre: item.genre,
          bpm: item.bpm,
          key: item.key,
          url: item.url,
          pricePerUnit: item.price,
        };

      case 'pack':
        return {
          productId: item.id,
          productType: 'pack',
          quantity,
          name: item.name,
          artist: item.artist,
          sampleCount: item.sampleCount,
          genres: item.genres,
          pricePerUnit: item.price,
        };

      default:
        // TypeScript ensures this never happens
        const _exhaustive: never = item;
        throw new Error(`Unknown item type: ${(_exhaustive as any).type}`);
    }
  });

  const totalQuantity = orderItems.reduce(
    (sum, item) => sum + item.quantity,
    0
  );

  // Calculate total price
  const totalPrice = orderItems.reduce(
    (sum, item) => sum + (item.pricePerUnit || 0) * item.quantity,
    0
  );

  return {
    items: orderItems,
    totalQuantity,
    totalPrice,
    status: "pending",
  };
}

